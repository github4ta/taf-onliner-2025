package it.academy.olyaabramovich;

public class Calculator {

    public int calculateSum(int a, int b) {
        return a + b;
    }

    public double calculateSum(double a, double b) {
        return a + b;
    }

    public double calculateSum(int a, double b) {
        return a + b;
    }

    public double calculateSum(double a, int b) {
        return a + b;
    }

    public String calculateSum(String str1, String str2) {
        return str1 + str2;
    }
}
