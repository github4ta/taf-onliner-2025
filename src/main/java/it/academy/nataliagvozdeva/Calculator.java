package it.academy.nataliagvozdeva;

public class Calculator {

    public int sum(int a, int b){
        return a + b;
    }
    public int subtraction(int a, int b){
        return a - b;
    }
    public int division(int a, int b){
        return a / b;
    }
    public int multiplication(int a, int b){
        return a * b;
    }

}
